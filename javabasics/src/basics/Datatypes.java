package basics;

public class Datatypes {
	public static void main(String args[]) {
		System.out.println("Learning Data Types here");

		// Primitive Datatypes

		byte a = 56;
		System.out.println(a);

		short b = 25000;
		System.out.println(b);

		int c = 100000;
		System.out.println(c);

		long d = 9004510238l; // in the end ,l says it's long otherwise long will consider it as int
		System.out.println(d);

		float e = 45.56f; // in the end, f says it's float otherwise float consider it as double
		System.out.println(e);

		double f = 45.561845625;
		System.out.println(f);

		boolean g = false;
		System.out.println(g);

		char h = '@';
		System.out.println(h);
	}
}

package _1_basics;

public class Example2 {
	public static void main(String[] args) {
//		Assignment Operator
		int a = 2;
		int b = 6;

		a += 10; // a=a+10;
		System.out.println(a);

		a -= 2; // a=a-2;
		System.out.println(a);

		a *= 6; // a=a*6
		System.out.println(a);

		a /= b; // a=a/b
		System.out.println(a);

		a %= b; // a=a%b;
		System.out.println(a);

	}
}

package _6_Inheritance;

public class Employee {
	int id;
	String name;
	String joiningDate;

	public Employee() {
		super();
	}

	public Employee(int id, String name, String joiningDate) {
		super();
		this.id = id;
		this.name = name;
		this.joiningDate = joiningDate;
	}

}

//   every Class is having object parent